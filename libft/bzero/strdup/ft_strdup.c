#include "libft.h"
#include <stdio.h>

char	*ft_strdup(const char *src)
{
	unsigned char	*dest;
	size_t			len;
	size_t			i;

	len = ft_strlen(src);
	dest = malloc(sizeof(char) * len + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
